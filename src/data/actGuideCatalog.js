function mc(q, choices, correct, exp) {
  return { q, choices, correct, exp }
}

function fr(q, correct, exp) {
  return { q, correct, exp }
}

function buildContent({ intro, lesson, strategy, check, problems }) {
  return {
    intro,
    concepts: [
      { title: 'What to look for', body: lesson, type: 'rule' },
      { title: 'How to attack it', body: strategy, type: 'tip' },
      { title: 'Quick self-check', body: check, type: 'rule' },
    ],
    problems,
  }
}

function englishModule({ id, code, name, range, intro, lesson, strategy, check, problems }) {
  const allProblems = ACT_ALL_QUESTIONS[id] || problems
  return {
    id,
    code,
    sectionId: 'act_english',
    subject: 'English',
    domain: 'ACT English',
    color: '#2563eb',
    questions: range,
    ...buildContent({ intro, lesson, strategy, check, problems: allProblems }),
    name,
  }
}

